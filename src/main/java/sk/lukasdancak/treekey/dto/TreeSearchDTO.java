package sk.lukasdancak.treekey.dto;


public class TreeSearchDTO {
    String latinName;
    String slovakName;
    String description;
    String familyName;
    String habitusName;
    String rhytidomeName;
    String leafBladeShapeName;
    String leafVeinsTypeName;

    public String getLatinName() {
        return latinName;
    }

    public void setLatinName(String latinName) {
        this.latinName = latinName;
    }

    public String getSlovakName() {
        return slovakName;
    }

    public void setSlovakName(String slovakName) {
        this.slovakName = slovakName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getFamilyName() {
        return familyName;
    }

    public void setFamilyName(String familyName) {
        this.familyName = familyName;
    }

    public String getHabitusName() {
        return habitusName;
    }

    public void setHabitusName(String habitusName) {
        this.habitusName = habitusName;
    }

    public String getRhytidomeName() {
        return rhytidomeName;
    }

    public void setRhytidomeName(String rhytidomeName) {
        this.rhytidomeName = rhytidomeName;
    }

    public String getLeafBladeShapeName() {
        return leafBladeShapeName;
    }

    public void setLeafBladeShapeName(String leafBladeShapeName) {
        this.leafBladeShapeName = leafBladeShapeName;
    }

    public String getLeafVeinsTypeName() {
        return leafVeinsTypeName;
    }

    public void setLeafVeinsTypeName(String leafVeinsTypeName) {
        this.leafVeinsTypeName = leafVeinsTypeName;
    }
}

